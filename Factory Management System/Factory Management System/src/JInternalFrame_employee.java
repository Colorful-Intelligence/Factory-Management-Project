    @Override
    public void showAll() {
        model.setRowCount(0);
        ArrayList<Employee> employees = new ArrayList<Employee>();
        
        employees = emp_operations.showEmployees();
        
        if (employees != null) {
            
            for(Employee element: employees){
                Object[] x = {element.getId(),element.getName(),element.getSurname(),element.getBirth_date(),element.getNationality(),element.getAddress(),element.getPhone(),element.getDpt_id(),element.getEmail(),element.getJob(),element.getSalary()};
                model.addRow(x);
                
            
            }
            
        }
        
    }

