    private void Button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_deleteActionPerformed
        
         int selectedrow = Table_suppliers.getSelectedRow();
         if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               label_message.setText("TABLE IS EMPTY");
           }
           else {
               label_message.setText("PLEASE CHOOSE A DEPARTMENT");
           }
      
       }
       else {
           int id = (int)model.getValueAt(selectedrow,0);
           
           supp_oOperations.delete(id);
           
           showAll();
           
           label_message.setText("DEPARTMENT IS DELETED SUCCESSFULLLY");
           clearAreas();
           
           
           
       }
        
    }//GEN-LAST:event_Button_deleteActionPerformed

    private void Button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_updateActionPerformed
       
        String name = TF_supp_name.getText();
        String country = String.valueOf(CB_supp_country.getSelectedItem().toString());
        String phone = TF_supp_phone.getText();
        String address = TA_supp_address.getText();
        String email = TF_supp_email.getText();
        
        int selectedrow = Table_suppliers.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                label_message.setText("TABLE IS EMPYT");
            }
            else {
                label_message.setText("PLEASE CHOOSE A SUPPLIER");
            }
        }
        else {
            
            
            
            
            int id = (int)model.getValueAt(selectedrow,0);
            
            
            
            supp_oOperations.updateSupplier(id, name, country, phone, address, email);
            
            showAll();
            
            label_message.setText("SUPPLIER IS UPDATE SUCCESSFULLLY");
        }
        
    }//GEN-LAST:event_Button_updateActionPerformed

