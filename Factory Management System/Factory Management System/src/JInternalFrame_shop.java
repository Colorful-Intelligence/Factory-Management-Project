    @Override
    public void showAll() {
        
        model.setRowCount(0);
        
       ArrayList<Shop> shop = new ArrayList<Shop>();
       
       shop = shop_operation.showShops();
       
       if (shop != null){
       
           for(Shop element : shop){
               
               Object[] x = {element.getShop_id(),element.getShop_name(),element.getShop_address(),element.getShop_email()};
               model.addRow(x);
           
           }
       
       }
        
        
    }

    @Override
    public void clearAreas() {
        TF_shop_id.setText("");
        TF_shop_name.setText("");
        TA_shop_address.setText("");
        TF_shop_email.setText("");
        
        
    }

    @Override
    public void dynamicSearch(String search) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        Table_shop.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(search));
        
    }
