    private void Button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_deleteActionPerformed
       int selectedrow = Table_shop.getSelectedRow();
         if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               label_message.setText("TABLE IS EMPTY");
           }
           else {
               label_message.setText("PLEASE CHOOSE A SHOP");
           }
      
       }
       else {
           int id = (int)model.getValueAt(selectedrow,0);
           
           shop_operation.delete(id);
           
           showAll();
           
           label_message.setText("SHOP IS DELETED SUCCESSFULLLY");
           clearAreas();
           
           
           
       }
    }//GEN-LAST:event_Button_deleteActionPerformed

    private void Button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_updateActionPerformed
        
        String shop_name = TF_shop_name.getText();
        String shop_address = TA_shop_address.getText();
        String shop_email = TF_shop_email.getText();
        
        
        int selectedrow = Table_shop.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                label_message.setText("TABLE IS EMPYT");
            }
            else {
                label_message.setText("PLEASE CHOOSE A SHOP");
            }
        }
        else {
            
            
            
            
            int id = (int)model.getValueAt(selectedrow,0);
            
            
            
            shop_operation.updateShop(id, shop_name, shop_address, shop_email);
            
            showAll();
            
            label_message.setText("SHOP IS UPDATE SUCCESSFULLLY");
        }
    }//GEN-LAST:event_Button_updateActionPerformed

