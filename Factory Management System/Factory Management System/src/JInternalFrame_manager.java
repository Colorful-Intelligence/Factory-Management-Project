    @Override
    public void showAll() {
        
       model.setRowCount(0);
        
       ArrayList<Manager> manager = new ArrayList<Manager>();
       
       manager = mo.showManagers();
       
       if(manager != null){
           
           for(Manager element : manager){
               
               Object[] x = {element.getMngr_id(),element.getMngr_name(),element.getMngr_surname(),element.getMngr_email(),element.getDpt_id(),element.getAddress(),element.getSalary(),element.getMngr_password()};
               model.addRow(x);
           
           }
       
       
       
       
       }
        
        
    }

