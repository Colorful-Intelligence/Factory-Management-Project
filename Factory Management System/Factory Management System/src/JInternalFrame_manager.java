    private void Button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_deleteActionPerformed
        
        
        int selectedrow = Table_manager.getSelectedRow();
         if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               label_message.setText("TABLE IS EMPTY");
           }
           else {
               label_message.setText("PLEASE CHOOSE A MANAGER");
           }
      
       }
       else {
           int id = (int)model.getValueAt(selectedrow,0);
           
           mo.delete(id);
           
           showAll();
           
           label_message.setText("MANAGER IS DELETED SUCCESSFULLLY");
           clearAreas();
           
           
           
       }
        
        
    }//GEN-LAST:event_Button_deleteActionPerformed

    private void Button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_updateActionPerformed
        
        
        String name = TF_name.getText();
        String surname = TF_surname.getText();
        String email = TF_email.getText();
        int dpt_id = Integer.parseInt(TF_dpt_id.getText());
        String address = TA_address.getText();
        float salary = Float.parseFloat(TF_salary.getText());
        String password = TF_password.getText();
        
        
        int selectedrow = Table_manager.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                label_message.setText("TABLE IS EMPYT");
            }
            else {
                label_message.setText("PLEASE CHOOSE A MANAGER");
            }
        }
        else {
            
            
            
            
            int id = (int)model.getValueAt(selectedrow,0);
            
            
            
            mo.updateManager(id, name, surname, email, dpt_id, address, salary, password);
            
            showAll();
            
            label_message.setText("MANAGER IS UPDATE SUCCESSFULLLY");
        }
        
        
    }//GEN-LAST:event_Button_updateActionPerformed

