    private void Button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_deleteActionPerformed
        int selectedrow = Table_account.getSelectedRow();
         if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               label_message.setText("TABLE IS EMPTY");
           }
           else {
               label_message.setText("PLEASE CHOOSE A REPORT");
           }
      
       }
       else {
           int id = (int)model.getValueAt(selectedrow,0);
           
           accountOperations.delete(id);
           
           showAll();
           
           label_message.setText("REPORT IS DELETED SUCCESSFULLLY");
           clearAreas();
           
           
           
       }
    }//GEN-LAST:event_Button_deleteActionPerformed

    private void Button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_updateActionPerformed
        if(TF_net_profit.getText().isEmpty() == false){
            
            float number_of_products_sold = Float.parseFloat(TF_number_of_prd_sold.getText());
            float revenue_from_sale = Float.parseFloat(TF_total_revenue_from_sale.getText());
            float total_amount_of_paid_salary = Float.parseFloat(TF_amount_of_paid_salary.getText());
            float other_expense = Float.parseFloat(TF_other_expense.getText());
            float net_profit = Float.parseFloat(TF_net_profit.getText());
            
            int selectedrow = Table_account.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                label_message.setText("TABLE IS EMPYT");
            }
            else {
                label_message.setText("PLEASE CHOOSE A REPORT");
            }
        }
        else {
            
            
            
            
            int id = (int)model.getValueAt(selectedrow,0);
            
            
            
            accountOperations.updateReport(id, number_of_products_sold, revenue_from_sale, total_amount_of_paid_salary, other_expense, net_profit);
            
            showAll();
            
            label_message.setText("REPORT IS UPDATE SUCCESSFULLLY");
        }
            
       
            
        
        
        
        
        }
        
        else{
        
        
        }
    }//GEN-LAST:event_Button_updateActionPerformed


