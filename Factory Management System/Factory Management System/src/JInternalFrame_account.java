    public void Calculate_Number_of_Product_Sold(){
        
        int amount = accountOperations.CalculateNumberOfProductsSold();
        String quantity = String.valueOf(amount);
        
        TF_number_of_prd_sold.setText(quantity);
        
    
    
    }
    
    public void CalculateTheIncomeFromSales(){
        
        float income = accountOperations.CalculateTotalRevenueFromSale();
        String INCOME = String.valueOf(income);
        TF_total_revenue_from_sale.setText(INCOME);
    
    
    }
    
    public void CalculateTheTotalPaidSalary(){
    
        float total_paid_salary = accountOperations.CalculateTotalPaidSalary();
        String TOTAL = String.valueOf(total_paid_salary);
        TF_amount_of_paid_salary.setText(TOTAL);
    
    }
    
    public void CalculateTheNetProfit(){
        
        
        float income = accountOperations.CalculateTotalRevenueFromSale();
        float total_paid_salary = accountOperations.CalculateTotalPaidSalary();
        float other_expenses = Float.parseFloat(TF_other_expense.getText());
        float net_profit = income - (total_paid_salary+other_expenses);
        String PROFIT = String.valueOf(net_profit);
        TF_net_profit.setText(PROFIT);
    
    }
    
    public void clearAreas(){
        
        TF_report_id.setText("");
        TF_number_of_prd_sold.setText("");
        TF_total_revenue_from_sale.setText("");
        TF_amount_of_paid_salary.setText("");
        TF_other_expense.setText("");
        TF_net_profit.setText("");
    
    
    }
    
    public void dynamicSearch(String search){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        Table_account.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(search));
    
    }
    
    public void showAll(){
        
       model.setRowCount(0);
        
       ArrayList<Account> account = new ArrayList<Account>();
       
       account = accountOperations.ShowReports();
       
       if(account != null){
       
           for(Account element : account){
           
               Object[] x = {element.getReport_id(),element.getTotal_sold_car_amount(),element.getTotal_income_from_car(),element.getTotal_paid_salary(),element.getTotal_bills(),element.getNet_profit()};
               model.addRow(x);
           }
       
       
       
       
       }
    
    
    }
    
    
