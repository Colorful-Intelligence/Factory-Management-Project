    @Override
    public void showAll() {
        model.setRowCount(0);
        
        ArrayList<Department> departments = new ArrayList<Department>();
        
        departments = dpt_operation.ShowDepartments();
        
        if(departments != null){
            
            for(Department element:departments){
            Object[] x = {element.getDpt_id(),element.getName(),element.getPhone(),element.getAddress()};
            model.addRow(x);
            
            }
        
        
        }
        
        
    }

    @Override
    public void clearAreas() {
        TF_dpt_id.setText("");
        TF_dpt_name.setText("");
        TF_dpt_phone.setText("XXX-XXX-XXXX");
        TextArea_dpt_address.setText("");
        label_message.setText("");
    }

    @Override
    public void dynamicSearch(String search) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        
        
        Table_Department.setRowSorter(tr);
        
        
        tr.setRowFilter(RowFilter.regexFilter(search));
    }
