    private void Button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_deleteActionPerformed
        
        int selectedrow = Table_Department.getSelectedRow();
         if (selectedrow == -1) {
           if (model.getRowCount() == 0 ) {
               label_message.setText("TABLE IS EMPTY");
           }
           else {
               label_message.setText("PLEASE CHOOSE A DEPARTMENT");
           }
      
       }
       else {
           int id = (int)model.getValueAt(selectedrow,0);
           
           dpt_operation.delete(id);
           
           showAll();
           
           label_message.setText("DEPARTMENT IS DELETED SUCCESSFULLLY");
           clearAreas();
           
           
           
       }
        
        
    }//GEN-LAST:event_Button_deleteActionPerformed

    private void Button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_updateActionPerformed
        
        String name = TF_dpt_name.getText();
        String phone = TF_dpt_phone.getText();
        String address = TextArea_dpt_address.getText();
        
        int selectedrow = Table_Department.getSelectedRow();
        
        if (selectedrow == -1) {
            
            if (model.getRowCount() == 0) {
                label_message.setText("TABLE IS EMPYT");
            }
            else {
                label_message.setText("PLEASE CHOOSE A DEPARTMENT");
            }
        }
        else {
            
            
            
            
            int id = (int)model.getValueAt(selectedrow,0);
            
            
            
            dpt_operation.updateDepartment(id,name,phone,address);
            
            showAll();
            
            label_message.setText("DEPARTMENT IS UPDATE SUCCESSFULLLY");
        }
        
        
    }//GEN-LAST:event_Button_updateActionPerformed

